# 대입은 단독코드로 사용해야 한다.
# 함수 외부에서 코드가 처리가 되야 되고....
# 함수를 이용해 결과를 본다.
result=50
result+=6
print("result += 6 :",result)
result-=6
print("result -= 6 :",result)
result*=6
print("result *= 6 :",result)
result/=6
print("result /= 6 :" ,result)
result//=6
print("result //= 6 :",result)
result %= 6
print("result %= 6 :" ,result)
result**=6
print("result **= 6 :",result)
